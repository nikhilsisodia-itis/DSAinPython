# Variables are used to store data that can be used later in the program.
# Variables are containers for storing data values.

# Python has no command for declaring a variable.
# A variable is created the moment you first assign a value to it.
# Variables do not need to be declared with any particular type and can even change type after they have been set.
# Variable names are case-sensitive.

# Example: 
greeting = "Namaste, World!"
# Here, greeting is a variable that stores the string "Namaste, World!".
# The variable name is greeting.
# The value stored in the variable is "Namaste, World!".

# Variable declaration: It means telling the program that a variable exists and the type of data it contains.
# Example: int currency, str word, float bill.

# Variable definition: It means assigning a value to the variable.
# Example: currency = 100, word = "Namaste", bill = 100.50.

# In Python, Variable declaration and definition happen at the same time.
# Example: currency = 100, word = "Namaste", bill = 100.50.
